const organizationsSlice = createSlice({
  name: 'organizations',
  initialState,
  reducers: {},
  extraReducers: (builder) => {
    builder
      // Fetch Tokens
      .addCase(fetchOrganizationThunk.pending, (state) => {
        state.loading = true;
        state.error = null;
      })
      .addCase(fetchOrganizationThunk.fulfilled, (state, action) => {
        state.organizations = [action.payload];
        state.loading = false;
      })
      .addCase(fetchOrganizationThunk.rejected, (state, action) => {
        state.loading = false;
        state.error = action.payload || 'Failed to fetch evmChains';
      });
  },
});

export default organizationsSlice.reducer;
